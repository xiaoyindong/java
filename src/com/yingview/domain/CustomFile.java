package com.yingview.domain;

public class CustomFile {
	private int fileId;
	private String filecode;
	private String filename;
	private int userId;
	private String filemime;
	private int articalId;
	private String filedownAdd;
	private String fileviewAdd;
	public CustomFile() {
		super();
	}
	public int getFileId() {
		return fileId;
	}
	public void setFileId(int fileId) {
		this.fileId = fileId;
	}
	public String getFilecode() {
		return filecode;
	}
	public void setFilecode(String filecode) {
		this.filecode = filecode;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getFilemime() {
		return filemime;
	}
	public void setFilemime(String filemime) {
		this.filemime = filemime;
	}
	public int getArticalId() {
		return articalId;
	}
	public void setArticalId(int articalId) {
		this.articalId = articalId;
	}
	public String getFiledownAdd() {
		return filedownAdd;
	}
	public void setFiledownAdd(String filedownAdd) {
		this.filedownAdd = filedownAdd;
	}
	public String getFileviewAdd() {
		return fileviewAdd;
	}
	public void setFileviewAdd(String fileviewAdd) {
		this.fileviewAdd = fileviewAdd;
	}
	
}
